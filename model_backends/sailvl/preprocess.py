from __future__ import annotations

from typing import Dict

import torch
import torchvision.transforms as T
from PIL import Image
from torchvision.transforms.functional import InterpolationMode


IMAGENET_MEAN = (0.485, 0.456, 0.406)
IMAGENET_STD = (0.229, 0.224, 0.225)


def get_sail_preprocess_config(config: Dict) -> Dict[str, int | bool]:
    vision_cfg = config.get("model", {}).get("vision", {})
    return {
        "image_size": int(vision_cfg.get("force_image_size", 448)),
        "use_thumbnail": bool(vision_cfg.get("use_thumbnail", True)),
        "min_dynamic_patch": int(vision_cfg.get("min_dynamic_patch", 1)),
        "max_dynamic_patch": int(vision_cfg.get("max_dynamic_patch", 10)),
    }


def build_transform(input_size: int):
    return T.Compose(
        [
            T.Lambda(lambda img: img.convert("RGB") if img.mode != "RGB" else img),
            T.Resize((input_size, input_size), interpolation=InterpolationMode.BICUBIC),
            T.ToTensor(),
            T.Normalize(mean=IMAGENET_MEAN, std=IMAGENET_STD),
        ]
    )


def find_closest_aspect_ratio(aspect_ratio, target_ratios, width, height, image_size):
    best_ratio_diff = float("inf")
    best_ratio = (1, 1)
    area = width * height
    for ratio in target_ratios:
        target_aspect_ratio = ratio[0] / ratio[1]
        ratio_diff = abs(aspect_ratio - target_aspect_ratio)
        if ratio_diff < best_ratio_diff:
            best_ratio_diff = ratio_diff
            best_ratio = ratio
        elif ratio_diff == best_ratio_diff:
            if area > 0.5 * image_size * image_size * ratio[0] * ratio[1]:
                best_ratio = ratio
    return best_ratio


def dynamic_preprocess(image: Image.Image, min_num=1, max_num=10, image_size=448, use_thumbnail=True):
    orig_width, orig_height = image.size
    aspect_ratio = orig_width / orig_height
    target_ratios = set(
        (i, j)
        for n in range(min_num, max_num + 1)
        for i in range(1, n + 1)
        for j in range(1, n + 1)
        if i * j <= max_num and i * j >= min_num
    )
    target_ratios = sorted(target_ratios, key=lambda x: x[0] * x[1])
    target_aspect_ratio = find_closest_aspect_ratio(
        aspect_ratio, target_ratios, orig_width, orig_height, image_size
    )
    target_width = image_size * target_aspect_ratio[0]
    target_height = image_size * target_aspect_ratio[1]
    blocks = target_aspect_ratio[0] * target_aspect_ratio[1]

    resized_img = image.resize((target_width, target_height))
    processed_images = []
    for i in range(blocks):
        box = (
            (i % (target_width // image_size)) * image_size,
            (i // (target_width // image_size)) * image_size,
            ((i % (target_width // image_size)) + 1) * image_size,
            ((i // (target_width // image_size)) + 1) * image_size,
        )
        processed_images.append(resized_img.crop(box))
    if use_thumbnail and len(processed_images) != 1:
        processed_images.append(image.resize((image_size, image_size)))
    return processed_images


def preprocess_sail_image(image: Image.Image, config: Dict) -> torch.Tensor:
    preprocess_cfg = get_sail_preprocess_config(config)
    transform = build_transform(preprocess_cfg["image_size"])
    images = dynamic_preprocess(
        image,
        min_num=preprocess_cfg["min_dynamic_patch"],
        max_num=preprocess_cfg["max_dynamic_patch"],
        image_size=preprocess_cfg["image_size"],
        use_thumbnail=preprocess_cfg["use_thumbnail"],
    )
    pixel_values = [transform(img) for img in images]
    return torch.stack(pixel_values)
